class JewelryModel {
  final String id;
  final String name;
  final double price;
  final String type;
  final String size;
  final String material;
  final String color;
  final String description;

  JewelryModel({
    required this.id,
    required this.name,
    required this.price,
    required this.type,
    required this.size,
    required this.material,
    required this.color,
    required this.description,
  });
}
